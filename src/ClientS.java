
public class ClientS {
	public ClientS(String username, int Id){
		this.username = username;
		this.Id = Id;
	}
	
	private String username;
	private int Id;
	
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	} 
}
